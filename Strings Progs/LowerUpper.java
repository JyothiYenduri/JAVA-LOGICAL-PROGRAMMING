import java.util.*;
class LowerUpper{
  public static void main(String args[]){
    String st1,st2;
    Scanner sc=new Scanner(System.in);
    System.out.println("Enter 2 strings: ");
    st1=sc.nextLine();
    st2=sc.nextLine();
    System.out.println("After converting first string into upper case: "+st1.toUpperCase());
    System.out.println("After converting second string into lower case: "+st2.toLowerCase());
  }
}
    
    